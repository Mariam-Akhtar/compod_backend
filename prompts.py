ACCOUNTABILITY_DESCRIPTORS = """
ACCOUNTABILITY EVALUATION FRAMEWORK

Accountability is the answerability for an action and for the consequences of that action. It is the measured effect of the job on end results. It has three dimensions: Freedom to Act, Magnitude/Area of Impact, and Nature of Impact.

===== SUB-FACTOR 1: FREEDOM TO ACT (ACC1) =====
Definition: The degree of personal, managerial or procedural control and guidance under which the role operates.

E-/E/E+
- Job, by its nature or size, is subject to broad practices and procedures covered by functional precedents and policies, achievement of a circumscribed operational activity and managerial direction
- Significant decision-making latitude for operational or functional activities managed by jobholder
- Retrospective review of operational activities against agreed targets, e.g. budgets
- Typical roles: mainly managerial positions or senior professional positions

F-/F/F+
- Job, by its nature or size, is broadly subject to functional policies and goals, and general managerial direction
- Accountable for developing functional policies of a significant unit or strategy for a business
- Typical roles: mainly senior management positions with strategic focus

=== ACC1 GATING RULES ===
1. The level reflects the DEGREE OF CONTROL AND GUIDANCE the role operates under, not the seniority of the job title. A senior title does not automatically justify a higher Freedom to Act level.

2. Apply these signals to anchor your assessment:
- Broad functional precedents; retrospective review against targets → E
- Functional policies and goals; develops policy for a unit → F

3. The existence of policies or regulations does not automatically lower the level. The question is whether those policies PRESCRIBE the actions taken or merely SET THE BOUNDARY within which the role exercises discretion.

4. Assign the fit sign based on where within the chosen level the role sits:
   - Clearly at the lower end of the level → use (-)
   - Solidly within the level              → use neutral
   - At the upper end of the level         → use (+)

=== ACC1 ANTI-INFLATION GUARDRAILS ===
The following reasoning patterns must not be used:
- "The role is senior therefore Freedom to Act is higher" — INVALID.
- "The role has wide responsibilities therefore it must be F" — INVALID. Breadth of responsibility does not determine the degree of guidance the role operates under.
- "The role makes important decisions therefore it is less controlled" — INVALID. Importance of decisions is captured in ACC2 and ACC3, not ACC1.

VALID reasons to assign F:
- The role itself develops or sets functional policy, not merely operates within it.
- The role defines strategy for a sizeable or diversified business or portfolio.
- Oversight is genuinely limited to broad policy or top management guidance with no procedural or supervisory review of day-to-day actions.


===== SUB-FACTOR 2: MAGNITUDE / AREA OF IMPACT (ACC2) =====
Definition: A qualitative measure considering strategic importance, economic contribution, and impact of work to Singapore and Singaporeans.

2-/2/2+ 
- Decisions made by the job holder will have impact to citizens at the agency or organisational level.

3-/3/3+
- Decisions made by the job holder will have impact to citizens at the sectoral level.

4-/4/4+
- Decisions made by the job holder will have impact to citizens at the national level.

=== ACC2 GATING RULES ===
1. The level reflects the REACH OF IMPACT on citizens and Singapore, not the internal seniority or budget size of the role.

2. Apply these signals to anchor your assessment:
- Impact felt across the agency or organisation as a whole → 2
- Impact felt across an industry sector or cross-agency domain → 3
- Impact felt at the national level, affecting Singapore broadly → 4

3. Do not conflate internal organisational scope with external citizen impact. A role that manages a large internal team but whose decisions do not reach beyond the agency should not exceed Level 2

4. Assign the fit sign (-, neutral, +) based on where within the chosen level the role sits

=== ACC2 ANTI-INFLATION GUARDRAILS ===
The following reasoning patterns must not be used:
- "The role is in a nationally important agency therefore impact is national" — INVALID. The agency's importance does not determine the reach of the individual role's decisions
- "The role manages a large budget therefore impact is Level 3 or 4" — INVALID. Budget size is not a direct proxy for citizen impact
- "The role is senior therefore impact is national" — INVALID

VALID reasons to assign Level 3 or 4:
- The role's decisions directly shape policy, services, or outcomes that affect citizens across a sector or the nation.
- The consequences of the role's decisions extend materially beyond the boundaries of the employing agency.

===== SUB-FACTOR 3: NATURE OF IMPACT (ACC3) =====
Definition: The nature of the job's impact on end results, direct or indirect, and the degree of control or contribution involved.
- Direct impact: Prime (P) and Shared (S)
- Indirect impact: Contributory (C) and Remote (R)

P
- Controlling impact on end results, where shared accountability of others is subordinate.
- The role is the primary driver of the outcome; others' contributions support rather than co-determine the result.

S
- Participating with others (except own subordinates and superiors), within or outside the organisational unit, in taking action.
- The role shares accountability for outcomes with peers or cross-functional partners on roughly equal terms.

C
- Interpretative, advisory or facilitating services of use by others in taking action.
- The role influences outcomes indirectly by informing, advising, or enabling the decisions of others.

R
- Informational, recording or incidental services for use by others in relation to some important end result.
- The role's connection to the end result is distant and mediated through multiple layers of action by others.

=== ACC3 GATING RULES ===
1. The level reflects HOW the role connects to end results, not how important the role is. An important advisory role is still C, not P.

2. Apply these diagnostic questions to anchor your assessment:
   - Does the role make the final call on outcomes, with others in a supporting role? → P
   - Does the role share ownership of outcomes with peers on roughly equal terms? → S
   - Does the role advise, interpret, or facilitate so that others can act? → C
   - Does the role provide information or records that feed into others' decisions at a distance? → R

3. The presence of direct reports does not automatically mean Prime impact. A manager whose team delivers outputs that feed into a larger decision made elsewhere may still be C or S.

4. Cross-functional coordination or stakeholder engagement does not automatically mean Shared impact. If the role's primary mode is advisory or facilitative, it remains C.

=== ACC3 ANTI-INFLATION GUARDRAILS ===

The following reasoning patterns must not be used:

- "The role is senior therefore it has Prime impact" — INVALID.
- "The role coordinates with many stakeholders therefore it is Shared"— INVALID. Coordination is not the same as shared accountability for outcomes.
- "The role provides important advice therefore it is Prime or Shared"— INVALID. Advisory roles are Contributory regardless of the importance of the advice.

VALID reasons to assign P:
- The role has final decision-making authority over the end result.
- Others' contributions are explicitly subordinate to the role's controlling accountability.

VALID reasons to assign S:
- The role co-owns outcomes with peers or external partners on genuinely equal terms, with no single party having controlling accountability.
"""


