HAY_PROFILE_SHAPE_RULE = """
HAY PROFILE SHAPE RULE
=====================================================================

A valid Hay evaluation must conform to the following profile shape
constraint, which reflects the structural logic of the methodology:

    Know-How (KH) ≥  Problem Solving (PS) ≥ Accountability (ACC) 

This is not a stylistic preference — it is a methodological constraint.
Roles where ACC exceeds PS, or where PS exceeds KH, represent an
internally inconsistent evaluation and must be corrected before
finalising.

Correction procedure:
1. If PS Total Score < ACC Total Points:
   - First, review whether ACC1/ACC2/ACC3 has been over-assigned.
   - Reduce ACC1(Freedom to Act) by one fit sign (e.g. E → E-) and recalculate.
   - If still non-conforming after one step, reduce by one full level.
   - Do not adjust PS upward to compensate unless independently justified.

2. If PS Total Points > KH Total Points:
   - First, review whether PS1 (Thinking Environment) has been over-assigned.
   - Reduce PS1 by one fit sign and recalculate.
   - If still non-conforming, reduce PS2 by one fit sign.
   - Do not adjust KH upward to compensate unless independently justified.

3. Document every adjustment made under this rule in the comments field,
   stating the original score, the adjusted score, and the reason.

4. The profile shape rule takes precedence over individual dimension
   assessments. A dimension score that is individually defensible but
   produces a non-conforming profile must still be adjusted.
"""
